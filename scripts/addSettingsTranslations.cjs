// Function to add translations to a language file
function updateLanguageFile(langCode, langTranslations) {
  const filePath = path.join(__dirname, `../src/assets/Languages/${langCode}.json`);

  try {
    // Read existing translations
    const existing = JSON.parse(fs.readFileSync(filePath, 'utf8'));

    // Merge new translations
    const updated = { ...existing, ...langTranslations };

    // Sort keys alphabetically
    const sorted = {};
    Object.keys(updated).sort().forEach(key => {
      sorted[key] = updated[key];
    });

    // Write back to file
    fs.writeFileSync(filePath, JSON.stringify(sorted, null, 2) + '\n', 'utf8');

    console.log(`✓ Updated ${langCode}.json`);
  } catch (error) {
    console.error(`✗ Error updating ${langCode}.json:`, error.message);
  }
}

// Update all language files
console.log('Adding enhanced settings translations to all language files...\n');

Object.keys(translations).forEach(langCode => {
  updateLanguageFile(langCode, translations[langCode]);
});

console.log('\n✓ All translations have been added successfully!');