type LangCode = string;

let currentLang: LangCode = 'en';

/** Detect language from browser or URL parameter */
function detectLanguage(): LangCode {
  // Check URL param: ?lang=he
  const params = new URLSearchParams(window.location.search);
  const urlLang = params.get('lang');
  if (urlLang && translations[urlLang]) return urlLang;

  // Check localStorage
  const stored = localStorage.getItem('flow_lang');
  if (stored && translations[stored]) return stored;

  // Check browser language
  const browserLang = navigator.language.split('-')[0];
  if (translations[browserLang]) return browserLang;

  return 'en';
}

/** Initialize i18n */
export function initI18n(): void {
  currentLang = detectLanguage();
  updateDocumentDirection();
}

/** Get current language */
export function getLang(): LangCode {
  return currentLang;
}

/** Set language */
export function setLang(lang: LangCode): void {
  currentLang = lang;
  localStorage.setItem('flow_lang', lang);
  updateDocumentDirection();
  m.redraw();
}

/** Check if current language is RTL */
export function isRTL(): boolean {
  return currentLang === 'he' || currentLang === 'ar' || currentLang === 'fa';
}

/** Update document direction */
function updateDocumentDirection(): void {
  document.documentElement.dir = isRTL() ? 'rtl' : 'ltr';
  document.documentElement.lang = currentLang;
}

/** Translate a key, with optional interpolation */
export function t(key: string, params?: Record<string, string | number>): string {
  const langDict = translations[currentLang] ?? translations.en;
  let text = langDict[key] ?? translations.en[key] ?? key;

  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(new RegExp(`\\{\\{${k}\\}\\}`, 'g'), String(v));
    }
  }

  return text;
}

/** Get available languages */
export function getAvailableLanguages(): Array<{ code: string; name: string }> {
  return [
    { code: 'en', name: 'English' },
    { code: 'he', name: 'עברית' },
    { code: 'ar', name: 'العربية' },
  ];
}
