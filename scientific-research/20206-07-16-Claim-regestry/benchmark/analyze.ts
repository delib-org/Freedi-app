/**
 * Aggregates all results/*.jsonl into RESULTS.md: triplet accuracy per
 * condition with Wilson CIs, pipeline-threshold simulation, the derived A2
 * (embedding+judge) condition, McNemar paired tests, and per-dataset tables
 * side-by-side with the paper's published numbers.
 *
 * Usage: npx tsx analyze.ts
 */
import { writeFileSync } from 'node:fs';
import type { ClaimClassification } from '../../../functions/src/services/claim-registry-service';
import { readJsonl, resultsPath } from './lib/io';
import { fmtRate, mcnemarExact, wilson, type Rate } from './lib/metrics';

const MIN_CONFIDENCE = 0.6; // REGISTRY_MIN_CONFIDENCE in registryPass.ts
const PASS2_THRESHOLD = 0.6;
const PASS1_THRESHOLD = 0.85;
const VECTOR_SEARCH_FLOOR = 0.45;

interface CosineRow {
	id: string;
	dataset: string;
	cosRawMatch: number;
	cosRawDistractor: number;
	cosCtxMatch: number;
	cosCtxDistractor: number;
}

interface RegistryRow {
	id: string;
	dataset: string;
	condition: string;
	model: string;
	claimText: string;
	match: ClaimClassification;
	distractor: ClaimClassification;
}

interface CodebookRow {
	id: string;
	dataset: string;
	codebookSize: number;
	anchorRankMatch: number;
	anchorRankDistractor: number;
	match: ClaimClassification;
	distractor: ClaimClassification;
}

/** Production attach rule. */
function attaches(c: ClaimClassification): boolean {
	return c.relation === 'expresses' && c.matchedClusterId !== null && c.confidence >= MIN_CONFIDENCE;
}

function opposesDetected(c: ClaimClassification): boolean {
	return c.relation === 'opposes' && c.opposedClusterId !== null && c.confidence >= MIN_CONFIDENCE;
}

function rateOf<T>(rows: T[], predicate: (row: T) => boolean): Rate {
	return wilson(rows.filter(predicate).length, rows.length);
}

function perDataset<T extends { dataset: string }>(
	rows: T[],
	predicate: (row: T) => boolean,
): Map<string, Rate> {
	const groups = new Map<string, T[]>();
	for (const r of rows) {
		const arr = groups.get(r.dataset) ?? [];
		arr.push(r);
		groups.set(r.dataset, arr);
	}

	return new Map([...groups.entries()].map(([d, g]) => [d, rateOf(g, predicate)]));
}

function correctnessMap<T extends { id: string }>(
	rows: T[],
	correct: (r: T) => boolean,
): Map<string, boolean> {
	return new Map(rows.map((r) => [r.id, correct(r)]));
}

function mcnemarLine(name: string, a: Map<string, boolean>, bMap: Map<string, boolean>): string {
	let b = 0;
	let c = 0;
	let n = 0;
	for (const [id, aCorrect] of a) {
		const bCorrect = bMap.get(id);
		if (bCorrect === undefined) continue;
		n++;
		if (aCorrect && !bCorrect) b++;
		if (!aCorrect && bCorrect) c++;
	}
	const { p } = mcnemarExact(b, c);

	return `| ${name} | ${n} | ${b} | ${c} | ${p.toExponential(2)} |`;
}

function main(): void {
	// Headline stats come from the 875-triplet main file only; `dev:` rows are
	// harness smoke tests.
	const isMain = (r: { id: string }): boolean => r.id.startsWith('main:');
	const cosines = readJsonl<CosineRow>('cosines.jsonl').filter(isMain);
	const b1 = readJsonl<RegistryRow>('registry-single-B1.jsonl').filter(isMain);
	const b2 = readJsonl<RegistryRow>('registry-single-B2.jsonl').filter(isMain);
	const b2e = readJsonl<RegistryRow>('registry-single-B2E.jsonl').filter(isMain);
	const b2e2 = readJsonl<RegistryRow>('registry-single-B2E2.jsonl').filter(isMain);
	const d = readJsonl<RegistryRow>('registry-single-D.jsonl').filter(isMain);
	const c = readJsonl<CodebookRow>('registry-codebook-C.jsonl').filter(isMain);
	const ce = readJsonl<CodebookRow>('registry-codebook-CE.jsonl').filter(isMain);
	const ce2 = readJsonl<CodebookRow>('registry-codebook-CE2.jsonl').filter(isMain);

	const cosById = new Map(cosines.map((r) => [r.id, r]));
	const lines: string[] = [];
	const put = (s = ''): void => {
		lines.push(s);
	};

	put('# Claim-Registry Hard-Triplet Benchmark — Results');
	put();
	put(`Generated by analyze.ts. Attach rule: expresses ∧ confidence ≥ ${MIN_CONFIDENCE}.`);
	put();

	// ---- Condition A: cosine baseline -------------------------------------
	if (cosines.length > 0) {
		put(`## Condition A — Cosine baseline (text-embedding-3-small), n=${cosines.length}`);
		put();
		put('| Metric | Value |');
		put('|---|---|');
		put(`| Triplet accuracy, raw text (paper-comparable) | ${fmtRate(rateOf(cosines, (r) => r.cosRawMatch > r.cosRawDistractor))} |`);
		put(`| Triplet accuracy, ctx format (production input) | ${fmtRate(rateOf(cosines, (r) => r.cosCtxMatch > r.cosCtxDistractor))} |`);
		put(`| Match ≥ ${PASS2_THRESHOLD} ctx (would attach at Pass 2) | ${fmtRate(rateOf(cosines, (r) => r.cosCtxMatch >= PASS2_THRESHOLD))} |`);
		put(`| Distractor ≥ ${PASS2_THRESHOLD} ctx (FALSE attach at Pass 2) | ${fmtRate(rateOf(cosines, (r) => r.cosCtxDistractor >= PASS2_THRESHOLD))} |`);
		put(`| Match ≥ ${PASS1_THRESHOLD} ctx (Pass 1) | ${fmtRate(rateOf(cosines, (r) => r.cosCtxMatch >= PASS1_THRESHOLD))} |`);
		put(`| Distractor ≥ ${PASS1_THRESHOLD} ctx (FALSE attach at Pass 1) | ${fmtRate(rateOf(cosines, (r) => r.cosCtxDistractor >= PASS1_THRESHOLD))} |`);
		put(`| Pipeline-correct (match ≥ ${PASS2_THRESHOLD} ∧ distractor < ${PASS2_THRESHOLD}) | ${fmtRate(rateOf(cosines, (r) => r.cosCtxMatch >= PASS2_THRESHOLD && r.cosCtxDistractor < PASS2_THRESHOLD))} |`);
		put();
		put('Paper reference (Table 2, hard n=875): ST5-XL 48.3, e5-large-v2 26.7, BGE-large 6.3, all-mpnet 8.2. DPT-tuned ST5-XL: 80.0.');
		put();
	}

	// ---- Registry single-claim conditions ----------------------------------
	const singleConditions: Array<[string, RegistryRow[]]> = [
		['B1 — registry, raw-anchor claim, gpt-4o-mini', b1],
		['B2 — registry, generated canonical claim', b2],
		['B2E — registry, ENRICHED claim (canonical + explanation + exemplar)', b2e],
		['B2E2 — enriched + stance-caution prompt (Phase 0b)', b2e2],
		['D — registry, raw-anchor claim, gpt-4o', d],
	];
	for (const [title, rows] of singleConditions) {
		if (rows.length === 0) continue;
		put(`## Condition ${title}, n=${rows.length}`);
		put();
		put('| Metric | Value |');
		put('|---|---|');
		put(`| **Triplet accuracy** (match attaches ∧ distractor does not) | ${fmtRate(rateOf(rows, (r) => attaches(r.match) && !attaches(r.distractor)))} |`);
		put(`| Match recall (attaches) | ${fmtRate(rateOf(rows, (r) => attaches(r.match)))} |`);
		put(`| Distractor false-attach | ${fmtRate(rateOf(rows, (r) => attaches(r.distractor)))} |`);
		put(`| Distractor detected as "opposes" | ${fmtRate(rateOf(rows, (r) => opposesDetected(r.distractor)))} |`);
		put(`| Distractor "none" | ${fmtRate(rateOf(rows, (r) => !attaches(r.distractor) && !opposesDetected(r.distractor)))} |`);
		const meanConf = (sel: (r: RegistryRow) => number): string =>
			(rows.reduce((s, r) => s + sel(r), 0) / rows.length).toFixed(3);
		put(`| Mean confidence (match / distractor) | ${meanConf((r) => r.match.confidence)} / ${meanConf((r) => r.distractor.confidence)} |`);
		put();
	}

	// ---- Condition A2: embedding + judge (derived from B1 + cosines) -------
	if (b1.length > 0 && cosines.length > 0) {
		const paired = b1.filter((r) => cosById.has(r.id));
		for (const gate of [VECTOR_SEARCH_FLOOR, PASS2_THRESHOLD]) {
			const retrievedMatch = (r: RegistryRow): boolean => cosById.get(r.id)!.cosCtxMatch >= gate;
			const retrievedDistractor = (r: RegistryRow): boolean =>
				cosById.get(r.id)!.cosCtxDistractor >= gate;
			put(`## Condition A2 — Embedding gate ≥ ${gate} + judge (derived), n=${paired.length}`);
			put();
			put('| Metric | Value |');
			put('|---|---|');
			put(`| **Triplet accuracy** | ${fmtRate(rateOf(paired, (r) => retrievedMatch(r) && attaches(r.match) && !(retrievedDistractor(r) && attaches(r.distractor))))} |`);
			put(`| Match recall (retrieved ∧ attaches) | ${fmtRate(rateOf(paired, (r) => retrievedMatch(r) && attaches(r.match)))} |`);
			put(`| Match lost at the gate (judge would attach, never retrieved) | ${fmtRate(rateOf(paired, (r) => !retrievedMatch(r) && attaches(r.match)))} |`);
			put(`| Distractor false-attach (retrieved ∧ attaches) | ${fmtRate(rateOf(paired, (r) => retrievedDistractor(r) && attaches(r.distractor)))} |`);
			put();
		}
	}

	// ---- Condition C: full codebook ----------------------------------------
	const codebookConditions: Array<[string, CodebookRow[]]> = [
		['C — Full per-dataset codebook', c],
		['CE — Full codebook, ENRICHED claims (Phase 0)', ce],
		['CE2 — Enriched + stance-caution prompt (Phase 0b)', ce2],
	];
	for (const [cTitle, cRows] of codebookConditions) {
		if (cRows.length === 0) continue;
		put(`## Condition ${cTitle}, n=${cRows.length}`);
		put();
		const ownMatch = (r: CodebookRow): boolean =>
			r.match.matchedClusterId === r.id && r.match.confidence >= MIN_CONFIDENCE;
		const ownDistractor = (r: CodebookRow): boolean =>
			r.distractor.matchedClusterId === r.id && r.distractor.confidence >= MIN_CONFIDENCE;
		put('| Metric | Value |');
		put('|---|---|');
		put(`| **Triplet accuracy** (match → own claim ∧ distractor ↛ own claim) | ${fmtRate(rateOf(cRows, (r) => ownMatch(r) && !ownDistractor(r)))} |`);
		put(`| Match → own anchor claim | ${fmtRate(rateOf(cRows, ownMatch))} |`);
		put(`| Match → any claim (secondary; near-duplicates legitimate) | ${fmtRate(rateOf(cRows, (r) => attaches(r.match)))} |`);
		put(`| Distractor → own anchor claim (false attach) | ${fmtRate(rateOf(cRows, ownDistractor))} |`);
		put(`| Distractor → any claim | ${fmtRate(rateOf(cRows, (r) => attaches(r.distractor)))} |`);
		put(`| Distractor opposes own anchor claim | ${fmtRate(rateOf(cRows, (r) => r.distractor.opposedClusterId === r.id && r.distractor.confidence >= MIN_CONFIDENCE))} |`);
		const meanSize = cRows.reduce((s, r) => s + r.codebookSize, 0) / cRows.length;
		put(`| Mean codebook size | ${meanSize.toFixed(1)} |`);
		put();
	}

	// ---- McNemar paired tests ----------------------------------------------
	put('## Paired McNemar tests (triplet-correct, common ids)');
	put();
	put('| Comparison (A vs B) | n | A✓B✗ | A✗B✓ | p (exact) |');
	put('|---|---|---|---|---|');
	const b1Correct = (r: RegistryRow): boolean => attaches(r.match) && !attaches(r.distractor);
	const b1Map = correctnessMap(b1, b1Correct);
	if (b1.length && cosines.length) {
		put(mcnemarLine('B1 registry vs A cosine-raw', b1Map, correctnessMap(cosines, (r) => r.cosRawMatch > r.cosRawDistractor)));
		const gate = VECTOR_SEARCH_FLOOR;
		const a2Map = correctnessMap(b1, (r) => {
			const cos = cosById.get(r.id);
			if (!cos) return false;

			return (
				cos.cosCtxMatch >= gate &&
				attaches(r.match) &&
				!(cos.cosCtxDistractor >= gate && attaches(r.distractor))
			);
		});
		put(mcnemarLine('B1 registry vs A2 gate 0.45', b1Map, a2Map));
	}
	if (b1.length && b2.length) {
		put(mcnemarLine('B1 raw-anchor vs B2 generated-claim', b1Map, correctnessMap(b2, b1Correct)));
	}
	if (b2.length && b2e.length) {
		const b2eCorrect = (r: RegistryRow): boolean => attaches(r.match) && !attaches(r.distractor);
		put(mcnemarLine('B2 bare canonical vs B2E enriched (Phase 0)', correctnessMap(b2, b2eCorrect), correctnessMap(b2e, b2eCorrect)));
	}
	if (c.length && ce.length) {
		const cCorrect = (r: CodebookRow): boolean =>
			r.match.matchedClusterId === r.id &&
			r.match.confidence >= MIN_CONFIDENCE &&
			!(r.distractor.matchedClusterId === r.id && r.distractor.confidence >= MIN_CONFIDENCE);
		put(mcnemarLine('C bare codebook vs CE enriched (Phase 0)', correctnessMap(c, cCorrect), correctnessMap(ce, cCorrect)));
	}
	if (b2e.length && b2e2.length) {
		const ok = (r: RegistryRow): boolean => attaches(r.match) && !attaches(r.distractor);
		put(mcnemarLine('B2E enriched vs B2E2 stance-caution (Phase 0b)', correctnessMap(b2e, ok), correctnessMap(b2e2, ok)));
	}
	if (ce.length && ce2.length) {
		const ok = (r: CodebookRow): boolean =>
			r.match.matchedClusterId === r.id &&
			r.match.confidence >= MIN_CONFIDENCE &&
			!(r.distractor.matchedClusterId === r.id && r.distractor.confidence >= MIN_CONFIDENCE);
		put(mcnemarLine('CE enriched vs CE2 stance-caution (Phase 0b)', correctnessMap(ce, ok), correctnessMap(ce2, ok)));
	}
	if (b1.length && d.length) {
		put(mcnemarLine('B1 gpt-4o-mini vs D gpt-4o', b1Map, correctnessMap(d, b1Correct)));
	}
	if (b1.length && c.length) {
		const cMap = correctnessMap(c, (r) =>
			r.match.matchedClusterId === r.id &&
			r.match.confidence >= MIN_CONFIDENCE &&
			!(r.distractor.matchedClusterId === r.id && r.distractor.confidence >= MIN_CONFIDENCE),
		);
		put(mcnemarLine('B1 single-claim vs C full codebook', b1Map, cMap));
	}
	put();

	// ---- Per-dataset breakdown ---------------------------------------------
	put('## Per-dataset triplet accuracy');
	put();
	const datasets = [...new Set([...cosines.map((r) => r.dataset), ...b1.map((r) => r.dataset)])].sort();
	const colA = perDataset(cosines, (r) => r.cosRawMatch > r.cosRawDistractor);
	const colB1 = perDataset(b1, b1Correct);
	const colB2 = perDataset(b2, (r) => attaches(r.match) && !attaches(r.distractor));
	const colB2E = perDataset(b2e, (r) => attaches(r.match) && !attaches(r.distractor));
	const colD = perDataset(d, (r) => attaches(r.match) && !attaches(r.distractor));
	const codebookCorrect = (r: CodebookRow): boolean => {
		const own = r.match.matchedClusterId === r.id && r.match.confidence >= MIN_CONFIDENCE;
		const ownD = r.distractor.matchedClusterId === r.id && r.distractor.confidence >= MIN_CONFIDENCE;

		return own && !ownD;
	};
	const colC = perDataset(c, codebookCorrect);
	const colCE = perDataset(ce, codebookCorrect);
	put('| Dataset | A cosine-raw | B1 registry | B2 generated | B2E enriched | D gpt-4o | C codebook | CE enriched |');
	put('|---|---|---|---|---|---|---|---|');
	const cell = (m: Map<string, Rate>, dataset: string): string => {
		const r = m.get(dataset);

		return r ? `${(r.rate * 100).toFixed(1)}% (${r.k}/${r.n})` : '—';
	};
	for (const dataset of datasets) {
		put(
			`| ${dataset} | ${cell(colA, dataset)} | ${cell(colB1, dataset)} | ${cell(colB2, dataset)} | ${cell(colB2E, dataset)} | ${cell(colD, dataset)} | ${cell(colC, dataset)} | ${cell(colCE, dataset)} |`,
		);
	}
	put();
	put('Paper Table 3 reference — hard triplets: base ST5-XL 48.3%, DPT-tuned 80.0 ± 0.37%.');
	put();

	const out = resultsPath('../RESULTS.md');
	writeFileSync(out, lines.join('\n'));
	console.info(`Wrote ${out}`);
	console.info(lines.join('\n'));
}

main();
